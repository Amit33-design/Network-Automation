// ── Helper to build cabling for spine-leaf ────────────────────────────────────

function spineLeafCabling(
  spines: string[],
  leaves: string[],
  speed: string,
  cableType: string,
  pricePerUnit: number,
): CableLink[] {
  const links: CableLink[] = []
  spines.forEach(sp => {
    leaves.forEach(lf => {
      links.push({
        id: `${sp}-${lf}`,
        fromLayer: 'spine',
        toLayer: 'leaf',
        fromDevice: sp,
        toDevice: lf,
        cableType,
        speed,
        lengthM: 5,
        quantity: 1,
        pricePerUnit,
        totalPrice: pricePerUnit,
      })
    })
  })
  return links
}

function basicOptics(
  linkGroup: string,
  formFactor: string,
  speed: string,
  qty: number,
  priceUSD: number,
  vendor: string,
  partNumber: string,
): OpticsEntry {
  return {
    id: `${linkGroup}-${formFactor}`,
    linkGroup,
    formFactor,
    speed,
    reach: 'SR',
    priceUSD,
    quantity: qty,
    totalPrice: priceUSD * qty,
    vendor,
    partNumber,
  }
}

