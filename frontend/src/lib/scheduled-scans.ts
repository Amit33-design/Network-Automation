export function simulateScanHistory(
  watchers: WatcherConfig[],
  deviceCount: number,
  count: number = 12,
): ScanHistoryEntry[] {
  const entries: ScanHistoryEntry[] = []
  const now = Date.now()
  const enabled = watchers.filter(w => w.enabled)
  if (enabled.length === 0 || deviceCount === 0) return entries

  for (let i = 0; i < count; i++) {
    const w = enabled[i % enabled.length]
    const hoursAgo = i * (w.intervalMinutes / 60) + 0.5
    const ts = new Date(now - hoursAgo * 3600_000)

    const hasDrift = i % 5 === 2 || i % 7 === 3
    const compScore = w.scanType !== 'drift'
      ? 70 + ((i * 7 + 13) % 31)
      : null
    const dCount = w.scanType !== 'compliance'
      ? (hasDrift ? 1 + (i % 3) : 0)
      : null

    const status: ScanHistoryEntry['status'] =
      (dCount !== null && dCount > 0) ? 'warn'
      : (compScore !== null && compScore < 80) ? 'warn'
      : 'ok'

    let detail: string
    if (w.scanType === 'compliance') {
      detail = compScore !== null && compScore < 80
        ? `Compliance score ${compScore}% — review required`
        : `Compliance score ${compScore}% — all clear`
    } else if (w.scanType === 'drift') {
      detail = dCount && dCount > 0
        ? `${dCount} device(s) drifted`
        : 'No drift detected'
    } else {
      const parts: string[] = []
      if (compScore !== null) parts.push(`compliance ${compScore}%`)
      if (dCount !== null && dCount > 0) parts.push(`${dCount} drifted`)
      else if (dCount !== null) parts.push('no drift')
      detail = parts.join(' · ')
    }

    entries.push({
      id: `scan-${i}`,
      watcherId: w.id,
      watcherName: w.name,
      scanType: w.scanType,
      timestamp: ts.toISOString(),
      durationMs: 800 + ((i * 31) % 4200),
      complianceScore: compScore,
      driftCount: dCount,
      deviceCount,
      status,
      detail,
    })
  }
  return entries
}

export function watcherSummaryText(watchers: WatcherConfig[]): string {
  const enabled = watchers.filter(w => w.enabled)
  if (enabled.length === 0) return 'No active watchers'
  const parts = enabled.map(w => {
    const preset = INTERVAL_PRESETS.find(p => p.minutes === w.intervalMinutes)
    const interval = preset?.label ?? `Every ${w.intervalMinutes}m`
    return `${w.name}: ${w.scanType} scan ${interval} → ${w.action}`
  })
  return parts.join('\n')
}
