import type { Compliance } from '@/types'

export type ScanType = 'compliance' | 'drift' | 'both'
export type ScanAction = 'report' | 'remediate' | 'alert'

export interface WatcherConfig {
  id: string
  name: string
  scanType: ScanType
  intervalMinutes: number
  frameworks: Compliance[]
  action: ScanAction
  enabled: boolean
  notifyEmail: string
  scope: 'all' | 'drifted'
}

export interface ScanHistoryEntry {
  id: string
  watcherId: string
  watcherName: string
  scanType: ScanType
  timestamp: string
  durationMs: number
  complianceScore: number | null
  driftCount: number | null
  deviceCount: number
  status: 'ok' | 'warn' | 'fail'
  detail: string
}

export const INTERVAL_PRESETS: Array<{ label: string; minutes: number }> = [
  { label: 'Every 15 min', minutes: 15 },
  { label: 'Every hour', minutes: 60 },
  { label: 'Every 4 hours', minutes: 240 },
  { label: 'Every 12 hours', minutes: 720 },
  { label: 'Daily', minutes: 1440 },
  { label: 'Weekly', minutes: 10080 },
]

let nextId = 1
export function createWatcher(partial: Partial<WatcherConfig> = {}): WatcherConfig {
  const id = `watcher-${nextId++}`
  return {
    id,
    name: partial.name ?? 'Untitled Watcher',
    scanType: partial.scanType ?? 'both',
    intervalMinutes: partial.intervalMinutes ?? 60,
    frameworks: partial.frameworks ?? [],
    action: partial.action ?? 'report',
    enabled: partial.enabled ?? true,
    notifyEmail: partial.notifyEmail ?? '',
    scope: partial.scope ?? 'all',
  }
}

function intervalToCron(minutes: number): string {
  if (minutes < 60) return `*/${minutes} * * * *`
  const h = Math.floor(minutes / 60)
  if (h < 24) return `0 */${h} * * *`
  const d = Math.floor(h / 24)
  if (d === 7) return '0 2 * * 0'
  return `0 2 */${d} * *`
}

export function exportCronTab(watchers: WatcherConfig[]): string {
  const lines = [
    '# NetDesign AI — Scheduled Compliance & Drift Scans',
    '# Generated by the Scheduled Scans watcher config',
    '# Install: crontab -e  (paste these lines)',
    '',
    'SHELL=/bin/bash',
    'PATH=/usr/local/bin:/usr/bin:/bin',
    'MAILTO=""',
    '',
  ]
  for (const w of watchers.filter(w => w.enabled)) {
    const cron = intervalToCron(w.intervalMinutes)
    const scanFlag = w.scanType === 'compliance' ? '--compliance-only'
      : w.scanType === 'drift' ? '--drift-only'
      : '--compliance --drift'
    const fwFlag = w.frameworks.length > 0 ? ` --frameworks ${w.frameworks.join(',')}` : ''
    const actionFlag = ` --action ${w.action}`
    const scopeFlag = w.scope === 'drifted' ? ' --scope drifted' : ''
    const mailFlag = w.notifyEmail ? ` --notify ${w.notifyEmail}` : ''
    lines.push(`# ${w.name} (${w.id})`)
    lines.push(`${cron}  /opt/netdesign/bin/scan-runner ${scanFlag}${fwFlag}${actionFlag}${scopeFlag}${mailFlag}`)
    lines.push('')
  }
  return lines.join('\n')
}

export function exportSystemdTimer(watcher: WatcherConfig): { timer: string; service: string } {
  const sec = watcher.intervalMinutes * 60
  const scanFlag = watcher.scanType === 'compliance' ? '--compliance-only'
    : watcher.scanType === 'drift' ? '--drift-only'
    : '--compliance --drift'
  const fwFlag = watcher.frameworks.length > 0 ? ` --frameworks ${watcher.frameworks.join(',')}` : ''
  const actionFlag = ` --action ${watcher.action}`
  const scopeFlag = watcher.scope === 'drifted' ? ' --scope drifted' : ''
  const mailFlag = watcher.notifyEmail ? ` --notify ${watcher.notifyEmail}` : ''

  const timer = [
    '[Unit]',
    `Description=NetDesign AI scan timer — ${watcher.name}`,
    '',
    '[Timer]',
    `OnBootSec=120`,
    `OnUnitActiveSec=${sec}`,
    'AccuracySec=60',
    '',
    '[Install]',
    'WantedBy=timers.target',
  ].join('\n')

  const service = [
    '[Unit]',
    `Description=NetDesign AI scan — ${watcher.name}`,
    '',
    '[Service]',
    'Type=oneshot',
    `ExecStart=/opt/netdesign/bin/scan-runner ${scanFlag}${fwFlag}${actionFlag}${scopeFlag}${mailFlag}`,
    'TimeoutStartSec=300',
    'StandardOutput=journal',
    'StandardError=journal',
  ].join('\n')

  return { timer, service }
}

