# ── Template registry ─────────────────────────────────────────────────────────

_TEMPLATES: dict[str, str] = {
    "cisco_ios":      _CISCO_IOS_TEMPLATE,
    "cisco_nxos":     _CISCO_NXOS_TEMPLATE,
    "juniper_junos":  _JUNIPER_JUNOS_TEMPLATE,
    "arista_eos":     _ARISTA_EOS_TEMPLATE,
}


# ── Validation helpers ────────────────────────────────────────────────────────

def _is_rfc1918(ip_str: str) -> bool:
    """Return True if the address falls in RFC1918 private space."""
    try:
        addr = ipaddress.ip_address(ip_str)
        return addr.is_private
    except ValueError:
        return False


def _vlans_overlap(vlans: list[VlanEntry]) -> list[str]:
    seen: set[int] = set()
    dupes: list[str] = []
    for v in vlans:
        if v.id in seen:
            dupes.append(str(v.id))
        seen.add(v.id)
    return dupes


def _prefixes_overlap(prefix_lists: list[PrefixList]) -> list[str]:
    """Detect prefix entries that are subnets/supernets of each other."""
    warnings: list[str] = []
    all_nets: list[tuple[str, ipaddress.IPv4Network | ipaddress.IPv6Network]] = []
    for pl in prefix_lists:
        for p in pl.prefixes:
            try:
                net = ipaddress.ip_network(p, strict=False)
                for prev_name, prev_net in all_nets:
                    if net.overlaps(prev_net):
                        warnings.append(
                            f"Prefix {p} in '{pl.name}' overlaps with {str(prev_net)} in '{prev_name}'"
                        )
                all_nets.append((pl.name, net))
            except ValueError:
                warnings.append(f"Invalid prefix: '{p}' in list '{pl.name}'")
    return warnings


# ── Generator class ───────────────────────────────────────────────────────────

class CustomPolicy:
    """Generates device configs from a CustomPolicyInput object."""

    _jinja_env = Environment(loader=BaseLoader(), trim_blocks=True, lstrip_blocks=True)

    def generate(self, policy_input: CustomPolicyInput) -> dict[str, str]:
        """
        Render a config for the specified device_type.

        Returns
        -------
        dict[str, str]
            Keys are device hostnames derived from policy name + device type.
            Values are full config text strings.
        """
        template_src = _TEMPLATES[policy_input.device_type]
        template = self._jinja_env.from_string(template_src)
        config_text = template.render(policy=policy_input)

        # Sanitise device name for use as a key
        safe_name = policy_input.name.replace(" ", "_").replace("/", "-").lower()
        hostname = f"{safe_name}-{policy_input.device_type}"

        return {hostname: config_text}

    @staticmethod
    def validate(policy_input: CustomPolicyInput) -> list[str]:
        """
        Return a list of warning strings (not errors) about the input.
        An empty list means no warnings.
        """
        warnings: list[str] = []

        # VLAN range check (1-4094 is enforced by Pydantic; flag reserved ones)
        for v in policy_input.vlans:
            if v.id in (0, 1, 4094, 4095):
                warnings.append(f"VLAN {v.id} is a reserved VLAN and may not be configurable on all platforms")

        # Duplicate VLANs
        dupes = _vlans_overlap(policy_input.vlans)
        for d in dupes:
            warnings.append(f"Duplicate VLAN ID: {d}")

        # BGP router-id RFC1918 check
        if policy_input.bgp:
            if not _is_rfc1918(policy_input.bgp.router_id):
                warnings.append(
                    f"BGP router-id {policy_input.bgp.router_id} is not an RFC1918 address — "
                    "ensure this is intentional for your topology"
                )
            # Large ASN hint
            if policy_input.bgp.asn > 4200000000:
                warnings.append(f"ASN {policy_input.bgp.asn} is above the IANA private ASN range (64512-65534 / 4200000000-4294967294)")

        # Prefix list overlap check
        warnings.extend(_prefixes_overlap(policy_input.prefix_lists))

        # NTP / DNS count advisory
        if len(policy_input.ntp_servers) == 1:
            warnings.append("Only one NTP server configured — consider adding a second for redundancy")
        if len(policy_input.dns_servers) == 1:
            warnings.append("Only one DNS server configured — consider adding a second for redundancy")

        return warnings
