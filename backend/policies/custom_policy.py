"""
Custom Policy Generator
========================
Allows users to define explicit network parameters and generate
production-ready device configs for multiple platforms.

Classes
-------
CustomPolicyInput  — Pydantic v2 input model with full validation
CustomPolicy       — Generator class; returns {device_hostname: config_text}

Supported platforms:
  cisco_ios   — IOS / IOS-XE (classic syntax)
  cisco_nxos  — NX-OS (data-center OS)
  juniper_junos — Junos (set-style commands)
  arista_eos  — Arista EOS
"""
from __future__ import annotations

import ipaddress
from typing import Literal, Optional

from jinja2 import Environment, BaseLoader
from pydantic import BaseModel, Field, field_validator


# ── Sub-models ───────────────────────────────────────────────────────────────

class VlanEntry(BaseModel):
    id: int = Field(..., ge=1, le=4094)
    name: str
    description: Optional[str] = None


class PeerGroup(BaseModel):
    name: str
    remote_as: int
    update_source: str = "Loopback0"
    peer_ips: list[str] = []


class BgpConfig(BaseModel):
    asn: int = Field(..., ge=1, le=4294967295)
    router_id: str
    peer_groups: list[PeerGroup] = []


class PrefixList(BaseModel):
    name: str
    action: Literal["permit", "deny"]
    prefixes: list[str] = []


class InterfaceConfig(BaseModel):
    name: str
    ip_address: str
    description: str = ""
    vlan_id: Optional[int] = None


# ── Top-level input model ─────────────────────────────────────────────────────

class CustomPolicyInput(BaseModel):
    name: str
    device_type: Literal["cisco_ios", "cisco_nxos", "juniper_junos", "arista_eos"]
    vlans: list[VlanEntry] = []
    bgp: Optional[BgpConfig] = None
    prefix_lists: list[PrefixList] = []
    interfaces: list[InterfaceConfig] = []
    ntp_servers: list[str] = []
    dns_servers: list[str] = []
    banner: Optional[str] = None

    @field_validator("name")
    @classmethod
    def name_not_empty(cls, v: str) -> str:
        v = v.strip()
        if not v:
            raise ValueError("policy name must not be empty")
        return v


