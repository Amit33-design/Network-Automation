/**
 * Generate OS image download/verification manifest for ZTP file server (G-32).
 * Returns a shell script that stages required images on the ZTP server.
 */
window.genOsImageManifest = function(devices, imageChannel) {
  imageChannel = imageChannel || 'stable';
  var lines = [
    '#!/bin/bash',
    '# ZTP OS Image Manifest — auto-generated by NetDesign AI',
    '# Channel: ' + imageChannel,
    '# Run on ZTP server to stage images for zero-touch provisioning',
    '',
    'set -euo pipefail',
    'IMAGE_BASE="/opt/ztp/images"',
    'HTTP_BASE="http://localhost:8080/ztp/images"',
    ''
  ];

  // Determine which platforms we need
  var platforms = {};
  (devices || []).forEach(function(dev) {
    var v = (dev.vendor || '').toLowerCase();
    var p = v.indexOf('cisco') !== -1 ? (
      (dev.subLayer === 'pe-router' || dev.subLayer === 'p-router') ? 'iosxr' :
      (dev.subLayer === 'access' || dev.subLayer === 'distribution') ? 'iosxe' : 'nxos'
    ) : v.indexOf('arista') !== -1 ? 'eos' :
        v.indexOf('juniper') !== -1 ? 'junos' :
        v.indexOf('sonic') !== -1 ? 'sonic' : null;
    if (p) platforms[p] = true;
  });

  Object.keys(platforms).forEach(function(platform) {
    var cat = window.OS_IMAGE_CATALOG[platform];
    if (!cat) return;
    var img = cat[imageChannel] || cat.stable;
    lines.push('# ── ' + platform.toUpperCase() + ' ──────────────────────────────────────');
    lines.push('mkdir -p "${IMAGE_BASE}/' + platform + '"');
    lines.push('IMAGE_PATH="${IMAGE_BASE}/' + platform + '/' + img.filename + '"');
    lines.push('if [ ! -f "${IMAGE_PATH}" ]; then');
    lines.push('  echo "[INFO] Image not found: ' + img.filename + ' (' + img.version + ')"');
    lines.push('  echo "[INFO] Place image at: ${IMAGE_PATH}"');
    lines.push('  echo "[INFO] Expected size: ~' + img.sizeGB + ' GB"');
    lines.push('  echo "[WARN] Image staging required before ZTP can provision ' + platform.toUpperCase() + ' devices"');
    lines.push('else');
    lines.push('  SIZE=$(stat -c%s "${IMAGE_PATH}" 2>/dev/null || echo 0)');
    lines.push('  echo "[OK]   ' + img.filename + ' staged (${SIZE} bytes)"');
    lines.push('fi');
    lines.push('');
  });

  lines.push('echo ""');
  lines.push('echo "Image manifest complete. Start ZTP stack with: docker compose -f ztp-docker-compose.yml up -d"');

  return lines.join('\n') + '\n';
};

/**
 * Generate per-device boot verification script for post-ZTP image check (G-32).
 */
