window.renderZtpImageTab = function(state) {
  var devices = state.devices || [];
  var lines = [
    '<h3 style="margin:0 0 12px">OS Image Version Management</h3>',
    '<p style="color:var(--text-dim);margin:0 0 16px;font-size:13px;">Stage OS images on the ZTP server before provisioning. Images are served via HTTP/TFTP to devices during ZTP.</p>',
    '<div style="display:flex;gap:12px;margin-bottom:16px;flex-wrap:wrap;">',
    '<label style="font-size:13px;font-weight:600;">Image Channel:',
    '<select id="ztp-img-channel" style="margin-left:8px;padding:4px 8px;background:var(--surface);color:var(--text);border:1px solid var(--border);border-radius:4px;">',
    '<option value="stable">Stable (Recommended)</option>',
    '<option value="latest">Latest</option>',
    '</select></label>',
    '<button class="btn btn-secondary" onclick="window.ztpDownloadImageManifest()">&#8595; Download Image Manifest</button>',
    '<button class="btn btn-secondary" onclick="window.ztpDownloadVersionCheck()">&#8595; Version Check Script</button>',
    '</div>'
  ];

  // Image table
  var platforms = {};
  devices.forEach(function(dev) {
    var v = (dev.vendor || '').toLowerCase();
    var p = v.indexOf('cisco') !== -1 ? (
      (dev.subLayer === 'pe-router' || dev.subLayer === 'p-router') ? 'iosxr' :
      (dev.subLayer === 'access' || dev.subLayer === 'distribution') ? 'iosxe' : 'nxos'
    ) : v.indexOf('arista') !== -1 ? 'eos' :
        v.indexOf('juniper') !== -1 ? 'junos' :
        v.indexOf('sonic') !== -1 ? 'sonic' : null;
    if (p) { platforms[p] = (platforms[p] || 0) + 1; }
  });

  if (Object.keys(platforms).length === 0) {
    lines.push('<p class="empty-state">No devices — complete Step 1 first.</p>');
  } else {
    lines.push('<div class="table-scroll"><table class="bom-table"><thead><tr>');
    lines.push('<th>Platform</th><th>Device Count</th><th>Stable Version</th><th>Latest Version</th><th>Image File</th><th>Size</th><th>Boot Command</th>');
    lines.push('</tr></thead><tbody>');
    Object.keys(platforms).forEach(function(p) {
      var cat = window.OS_IMAGE_CATALOG[p];
      if (!cat) return;
      lines.push('<tr>');
      lines.push('<td><strong>' + p.toUpperCase() + '</strong></td>');
      lines.push('<td>' + platforms[p] + '</td>');
      lines.push('<td><span style="color:#22c55e;">' + cat.stable.version + '</span></td>');
      lines.push('<td>' + cat.latest.version + '</td>');
      lines.push('<td style="font-family:monospace;font-size:11px;">' + cat.stable.filename + '</td>');
      lines.push('<td>' + cat.stable.sizeGB + ' GB</td>');
      lines.push('<td style="font-family:monospace;font-size:11px;">' + cat.stable.boot_cmd.replace('{filename}', cat.stable.filename) + '</td>');
      lines.push('</tr>');
    });
    lines.push('</tbody></table></div>');
  }
  return lines.join('\n');
};

